package sound;

/**
 * @author Ben Matthews, Nick Schrandt
 *
 * this enum contains sound identifiers for
 * different types of sound clips in the game
 *
 * new: added the last three values
 */
public enum Sound
{
  ACHIEVE, //player achieves something
  DEATH, //zombie attack
  FOOTSTEP, //player walking
  GROAN, //zombie groan
  BUTTON, //button sound
  SHUFFLE, //zombie walking
  SWING, //sword swinging
  PUNCH, //punch hits zombie
  PUSH, //push zombie
  GRUNT //player grunt
}
